/***********************************************************************************************************************
* Start of function osal_resolve_soc_dependencies()
***********************************************************************************************************************/
e_osal_return_t osal_resolve_soc_dependencies(void)
{
    /*------------------------------------------------------------------------------------------------------------------
    Local variables
    ------------------------------------------------------------------------------------------------------------------*/
    e_osal_return_t osal_ret = OSAL_RETURN_DEV;

    /*------------------------------------------------------------------------------------------------------------------
    Function body
    ------------------------------------------------------------------------------------------------------------------*/
    if (osal_device_info()
     && osal_interrupt_thread_config())
    {
        osal_ret = OSAL_RETURN_OK;
    }

    return osal_ret;
}
