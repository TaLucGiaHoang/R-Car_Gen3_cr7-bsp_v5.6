#ifndef __ARCH_HELPERS_S__
#define __ARCH_HELPERS_S__

	.globl	aif_enable_i
	.globl	aif_disable_i
	.globl	aif_enable_a
	.globl	aif_disable_a

/*****************************************************************************
 *	enable_aif
 *****************************************************************************/
aif_enable_i:
	cpsie i
	bx lr

/*****************************************************************************
 *	disable_aif
 *****************************************************************************/
 aif_disable_i:
	cpsid i
	bx lr

/*****************************************************************************
 *	enable_aif
 *****************************************************************************/
aif_enable_a:
	cpsie a
	bx lr

/*****************************************************************************
 *	disable_aif
 *****************************************************************************/
 aif_disable_a:
	cpsid a
	bx lr

	.end
